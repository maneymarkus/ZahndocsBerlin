						Am 1. Januar 2005 trat eine Neuregelung im Rahmen des Gesetzes zur Modernisierung der gesetzlichen Krankenversicherungen (GMG) in Kraft, die im Bereich des Zahnersatzes einiges geändert hat zum Vorteil für die Patienten.
					</p>
					
					<p>
						<strong>Stand 2006</strong><br />
						Seit Anfang 2005 beteiligen sich die gesetzlichen Krankenkassen zwar nicht an den Kosten für die chirurgische Einsetzen der Implantate, aber an den Kosten für deren Versorgung mit Kronen oder Prothesen. So haben die Versicherten der gesetzlichen Krankenkassen erstmals die Möglichkeit, sich für hochwertigen Zahnersatz durch eine Implantatversorgung zu entscheiden. Die Kassen zahlen feste Beträge je nach Befund unabhängig von der gewählten Therapie.
					</p>
					
					<p>
						Die Höhe des jeweiligen Zuschusses richtet sich nach den Kosten für die Regelversorgung, die für den jeweiligen Befund als „Standardtherapie“ festgelegt ist. Der Heil- und Kostenplan, der vor jeder Zahnersatz-Behandlung erstellt werden muss, wird der Krankenkasse vorgelegt und muss von ihr genehmigt werden. Die Kasse ermittelt dann die Festzuschüsse, die dem Versicherten erstattet werden.
					</p>
					
